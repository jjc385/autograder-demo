import numpy as np
import random

def return_integer(dummy1=1, dummy2=2) :
    return dummy1 + dummy2

def return_integer_list(dummy1=1, dummy2=2) :
    return [dummy1, dummy2]

def return_float(dummy1=1., dummy2=2.) :
    return dummy1 / dummy2

def return_float_list(dummy1=1., dummy2=2.) :
    return [dummy1, dummy2]

def return_float_list(dummy1=1., dummy2=2.) :
    return np.array([dummy1, dummy2])
